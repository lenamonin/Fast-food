    public MainKitchenUI(OrderListManager orderListManager) {
        this.orderListManager = orderListManager;
        this.orderListFrame = new OrderListFrame(this.orderListManager, this);
        this.setTitle("Cuisine");
        initComponents();
        this.setSize(800, 600);
        this.showListOrderFrame();
    }
    
    public void showListOrderFrame() {
        this.jPanel1.removeAll();
        this.jPanel1.add(this.orderListFrame);
        this.revalidate();
        this.repaint();
    }
    
    public void showOrderDetailFrame(Order order) {
        this.jPanel1.remove(this.orderListFrame);
        this.jPanel1.add(new OrderDetailPanel(order, this, this.orderListManager));
        
        this.revalidate();
        this.repaint();
    }

